#{
    ~label 'on'  0xD
    ~label 'off' 0xE
    ~label 'led' 0xB
}
.warm

.ldi &(0x2) <| $(0x11)
.ldi &(0x3) <| $(0x01)
.ldi &(0xE) <| $(0x0C)

; if 0x2 == 0xF then set 0x0
.ref_t &(0xD)
; bug
.sub &(0x2) &(0x2) &(0x2)

.ref_t &(0xA)
; enable disable LED
.mvd &(![~led]) &(![~on] ) |> &(0x2)
.mvd &(![~led]) &(![~off]) |> &(0x2)

; if 0xE(0xC value) >= 0x2 (0x2++)
.jump_e &(0xD) ~- &(0x2) &(0xE)

.add &(0x2) &(0x2) &(0x3)

.jump_t &(0xA)
.halt

